/* Initialize font - create GL texture atlas */
int font_init(void)
{
    unsigned char atlas[ATLAS_W * ATLAS_H * 2]; /* luminance + alpha */
    int ch, row, col, px, py, bit;
    const unsigned char *glyph;

    log_msg("font_init: creating texture atlas");

    memset(atlas, 0, sizeof(atlas));

    /* Render each character into the atlas */
    for (ch = 0; ch < NUM_CHARS; ch++) {
        col = ch % ATLAS_COLS;
        row = ch / ATLAS_COLS;
        glyph = &font_data[ch * CHAR_H];

        for (py = 0; py < CHAR_H; py++) {
            for (px = 0; px < CHAR_W; px++) {
                bit = (glyph[py] >> (7 - px)) & 1;
                if (bit) {
                    int tx = col * CHAR_W + px;
                    int ty = row * CHAR_H + py;
                    int idx = (ty * ATLAS_W + tx) * 2;
                    atlas[idx] = 255;     /* luminance */
                    atlas[idx + 1] = 255; /* alpha */
                }
            }
        }
    }

    /* Create GL texture */
    glGenTextures(1, &g_font_texture);
    glBindTexture(GL_TEXTURE_2D, g_font_texture);

    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_NEAREST);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);

    glTexImage2D(GL_TEXTURE_2D, 0, GL_LUMINANCE_ALPHA, ATLAS_W, ATLAS_H, 0,
                 GL_LUMINANCE_ALPHA, GL_UNSIGNED_BYTE, atlas);

    log_msg("font_init: texture atlas created");
    return 0;
}

/* Shutdown font */
void font_shutdown(void)
{
    if (g_font_texture) {
        glDeleteTextures(1, &g_font_texture);
        g_font_texture = 0;
    }
}

/* Draw a string at screen coordinates */
void font_draw_string(const char *text, float x, float y, int scale,
                      float r, float g, float b, float a)
{
    /* 6 vertices per char (2 triangles), 2 coords each = 12 floats per char */
    GLfloat verts[MAX_CHARS * 12];
    GLfloat texcs[MAX_CHARS * 12];
    int i, len, count;
    float cw, ch_f;
    float inv_aw, inv_ah;

    if (!text || !g_font_texture) return;

    len = strlen(text);
    if (len > MAX_CHARS) len = MAX_CHARS;
    if (len == 0) return;

    cw = (float)(CHAR_W * scale);
    ch_f = (float)(CHAR_H * scale);
    inv_aw = 1.0f / ATLAS_W;
    inv_ah = 1.0f / ATLAS_H;

    count = 0;
    for (i = 0; i < len; i++) {
        int c = (unsigned char)text[i];
        int col, row;
        float u0, v0, u1, v1;
        float px, py;

        if (c < FIRST_CHAR || c > LAST_CHAR) c = FIRST_CHAR; /* space for unknown */

        c -= FIRST_CHAR;
        col = c % ATLAS_COLS;
        row = c / ATLAS_COLS;

        u0 = col * CHAR_W * inv_aw;
        v0 = row * CHAR_H * inv_ah;
        u1 = u0 + CHAR_W * inv_aw;
        v1 = v0 + CHAR_H * inv_ah;

        px = x + i * cw;
        py = y;

        /* Triangle 1: top-left, top-right, bottom-left */
        verts[count * 12 + 0] = px;
        verts[count * 12 + 1] = py;
        verts[count * 12 + 2] = px + cw;
        verts[count * 12 + 3] = py;
        verts[count * 12 + 4] = px;
        verts[count * 12 + 5] = py + ch_f;

        texcs[count * 12 + 0] = u0;
        texcs[count * 12 + 1] = v0;
        texcs[count * 12 + 2] = u1;
        texcs[count * 12 + 3] = v0;
        texcs[count * 12 + 4] = u0;
        texcs[count * 12 + 5] = v1;

        /* Triangle 2: top-right, bottom-right, bottom-left */
        verts[count * 12 + 6] = px + cw;
        verts[count * 12 + 7] = py;
        verts[count * 12 + 8] = px + cw;
        verts[count * 12 + 9] = py + ch_f;
        verts[count * 12 + 10] = px;
        verts[count * 12 + 11] = py + ch_f;

        texcs[count * 12 + 6] = u1;
        texcs[count * 12 + 7] = v0;
        texcs[count * 12 + 8] = u1;
        texcs[count * 12 + 9] = v1;
        texcs[count * 12 + 10] = u0;
        texcs[count * 12 + 11] = v1;

        count++;
    }

    /* Render all characters in one draw call */
    glEnable(GL_TEXTURE_2D);
    glEnable(GL_BLEND);
    glBlendFunc(GL_SRC_ALPHA, GL_ONE_MINUS_SRC_ALPHA);

    glBindTexture(GL_TEXTURE_2D, g_font_texture);
    glColor4f(r, g, b, a);

    glEnableClientState(GL_VERTEX_ARRAY);
    glEnableClientState(GL_TEXTURE_COORD_ARRAY);

    glVertexPointer(2, GL_FLOAT, 0, verts);
    glTexCoordPointer(2, GL_FLOAT, 0, texcs);

    glDrawArrays(GL_TRIANGLES, 0, count * 6);

    glDisableClientState(GL_TEXTURE_COORD_ARRAY);
    glDisableClientState(GL_VERTEX_ARRAY);

    glDisable(GL_TEXTURE_2D);
}

/* Measure string width in pixels */
int font_string_width(const char *text, int scale)
{
    if (!text) return 0;
    return strlen(text) * CHAR_W * scale;
}
